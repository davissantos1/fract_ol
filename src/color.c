#include "fract_ol.h"

int	create_trgb(int t, int r, int g, int b)
{
	if (t < 0 || t > 255 || r < 0 || r > 255)
		return (-1);
	if (g < 0 || g > 255 || b < 0 || b > 255)
		return (-1);
	return (t << 24 | r << 16 | g << 8 | b);
}
